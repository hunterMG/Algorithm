#include <iostream>
#include <cstdio>
using namespace std;

int a[100009], b[100009], k[1011], p[1011];
int main()
{
    int m,n;
    while(~scanf("%d%d", &n, &m)){
        for(int i=0; i<n; i++)
            scanf("%d%d", &a[i], &b[i]);
        for(int i=0; i<m; i++)
            scanf("%d%d", &k[i], &p[i]);

    }
    return 0;
}
